<p>Els projectes Socials s'acompanyen d'un pla d'acció  que esdevé una formidable eina de desenvolupament social  i un gran a recurs pedagògic mitjançant arts escèniques i tallers de circ. Són projectes que prescriuen accions concretes de sensibilització per defensar els drets dels infants i que persegueixen els objectius del mil·lenni, per l'assoliment dels quals encara queda un llarg camí per recórrer. Volem promoure un món més obert i més digne per tothom, sobretot pel que fa la població infantil.</p>

<p>Som pallass@s, a través de les actuacions hi ha un contacte directe amb els veritables beneficiaris, els infants, i és en aquests moments en els que la nostra tasca pren tot el seu sentit.</p>  
